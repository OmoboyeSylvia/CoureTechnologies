using CoureTechnologies.Interface;
using Microsoft.EntityFrameworkCore;

namespace CoureTechnologies.Repository
{
    public abstract class GenericRepository<T> : IGeneric<T> where T : class
    {
        protected readonly CountryDbContext _context;

        protected GenericRepository(CountryDbContext context)
        {
            _context = context;
        }

        public async Task<T> Get(int id)
        {
            return await _context.Set<T>().FindAsync(id);
        }

        public async Task<IEnumerable<T>> GetAll()
        {
            return await _context.Set<T>().ToListAsync();
        }

        public async Task Add(T entity)
        {
            await _context.Set<T>().AddAsync(entity);
        }

        public void Delete(T entity)
        {
            _context.Set<T>().Remove(entity);
        }

        public void Update(T entity)
        {
            _context.Set<T>().Update(entity);
        }
    }
}
